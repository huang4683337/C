/**
 * 数据类型分为四大类型:
 * 		基本数据类型, 构造数据类型, 指针类型, 空类型
 */

/**
 * 基本数据类型可分为三类: 字符型 整形 浮点型
 * 
 * 		字符型(char): 占用一个字节, 用于存储单个字符, char sex = 'M'
 * 		
 * 		整形(int): 占用2个字节, 用于存储整数, int height = 18;
 * 		
 * 	 	单精度浮点型(float): 占用4个字节, 用于存储小数, float price = 11.1;
 * 	 	
 * 	 	双精度浮点型(double): 占用8个字符, 用于存储位数很多的小数, double pi = .1415926  
 */